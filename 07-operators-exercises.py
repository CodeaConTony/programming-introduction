# # Ejercicios con operadores

# # Intuye los resultados antes de ejecutar
x = 7
y = 10

print(x > y) # False

print(x < y) # True

print(x >= y) # False

print(x <= y) # True

x += 2 # 9

x -= 2 # 7

x *= 2 # 14

x /= 2 # 7

x //= 2 # 3

print(x) # 3

print('a' < 'b' and 'c' > 'd') # True - False -> False

print('a' < 'b' and 'd' > 'c') # True - True -> True

print(x > y or x < y) # False - True -> True
